# O objetivo desse programa é realizar a soma de numeros inteiros.
valor_inicial1 = int(input("Digite um número inteiro inicial para a soma: "))
valor_final1 = int(input("Digite um número final que seja inteiro: "))
resultado1 = (sum(range(valor_inicial1, valor_final1))) + valor_final1
#Então, apresentaremos o resultado da soma;    
print("\nSoma de Números inteiros:")
print("\nResultado da Soma 1 = ", resultado1)
   